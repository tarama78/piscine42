#include "../includes/libft_incl.h"

int		ft_strcmp(char *s1, char *s2)
{
	int		i;

	i = -1;
	while (s1[++i] != '\0')
	{
		if (s2[i] == '\0')
			return (s1[i]);
		else if (s1[i] > s2[i] || s1[i] < s2[i])
			return (s1[i] - s2[i]);
	}
	return (-s2[i]);
}
